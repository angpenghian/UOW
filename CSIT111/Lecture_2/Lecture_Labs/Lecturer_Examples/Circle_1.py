class Circle_1:
    def main(self):
        # Declare variables
        
        # Assign a value to radius
        radius = 123.4567
        
        # Compute its area and its perimeter
        area = 3.14159 * radius * radius
        perimeter = 2.0 * 3.14159 * radius
        
        # Display the results
        print("Given radius = " + str(radius))
        print("Area = " + str(area))
        print("Perimeter = " + str(perimeter))
        
circle_1 = Circle_1()
circle_1.main()